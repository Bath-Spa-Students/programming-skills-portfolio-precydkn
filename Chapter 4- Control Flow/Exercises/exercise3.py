## Exercise 3: Alien Colors #3 
'''Turn your if-else chain from Exercise 5-4 into an if-elif-else chain.
•	 If the alien is green, print a message that the player earned 5 points.
•	 If the alien is yellow, print a message that the player earned 10 points.
•	 If the alien is red, print a message that the player earned 15 points.
•	 Write three versions of this program, making sure each message is printed for the appropriate color alien.'''

## program for green alien
alien_color = "green"

#if-elif-else statement about the points earned based on the alien color
if alien_color == "green":
    print("Green Alien shot. You earn 5 points!") # displays 5 points earned if the alien is green
elif alien_color == "yellow":
    print("Yellow Alien shot. You earn 10 points!") # displays 10 points earned if the alien is yellow
else:
    print("Red Alien shot. You earn 15 points!") # displays 15 points earned if the alien is a different color

